using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.RazorPages;
using Microsoft.EntityFrameworkCore;
using RazorRestaurant.Data;
using RazorRestaurant.Pages.Models;

namespace RazorRestaurant.Pages.Menu
{
    public class DetailsModel : PageModel
    {
        private readonly RazorRestaurant.Data.RazorRestaurantContext _context;

        public DetailsModel(RazorRestaurant.Data.RazorRestaurantContext context)
        {
            _context = context;
        }

      public Product Product { get; set; } = default!; 

        public async Task<IActionResult> OnGetAsync(int? id)
        {
            if (id == null || _context.Product == null)
            {
                return NotFound();
            }

            var product = await _context.Product.FirstOrDefaultAsync(m => m.Id == id);
            if (product == null)
            {
                return NotFound();
            }
            else 
            {
                Product = product;
            }
            return Page();
        }
    }
}
